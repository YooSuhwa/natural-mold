async def test_rollback_revision_restores_content_and_creates_new_revision(
    client: AsyncClient,
    db: AsyncSession,
    tmp_path: Path,
) -> None:
    with patch.object(skill_service.settings, "data_root", str(tmp_path)):
        skill = await skill_service.create_text_skill(
            db,
            user_id=TEST_USER_ID,
            name="Notes",
            slug="notes",
            description="Use when summarizing notes.",
            content=_skill_content("Original guidance."),
        )
        original = await skill_revision_service.create_revision_for_skill(
            db,
            skill=skill,
            user_id=TEST_USER_ID,
            operation="create",
        )
        await skill_service.update_text_content(
            db,
            skill=skill,
            content=_skill_content("Changed guidance."),
        )
        await skill_revision_service.create_revision_for_skill(
            db,
            skill=skill,
            user_id=TEST_USER_ID,
            operation="manual_content_update",
        )
        await db.commit()

        response = await client.post(f"/api/skills/{skill.id}/revisions/{original.id}/rollback")
        content = await skill_service.read_text_content(skill)

    assert response.status_code == 200, response.text
    body = response.json()
    assert "Original guidance." in content
    assert body["revision"]["operation"] == "rollback"
    assert body["skill"]["id"] == str(skill.id)


