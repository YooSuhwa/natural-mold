from __future__ import annotations

from pathlib import Path
from unittest.mock import patch

import pytest
from httpx import AsyncClient
from sqlalchemy.ext.asyncio import AsyncSession

from app.services import skill_revision_service
from app.skills import service as skill_service
from tests.conftest import TEST_USER_ID

pytestmark = pytest.mark.asyncio


def _skill_content(body: str = "Use when summarizing meeting notes.") -> str:
    return (
        "---\n"
        "name: notes\n"
        'description: "Use when summarizing notes into action items."\n'
        "---\n\n"
        f"{body}\n"
    )


async def test_list_revisions_returns_owned_skill_history(
    client: AsyncClient,
    db: AsyncSession,
    tmp_path: Path,
) -> None:
    with patch.object(skill_service.settings, "data_root", str(tmp_path)):
        skill = await skill_service.create_text_skill(
            db,
            user_id=TEST_USER_ID,
            name="Notes",
            slug="notes",
            description="Use when summarizing notes.",
            content=_skill_content(),
        )
        revision = await skill_revision_service.create_revision_for_skill(
            db,
            skill=skill,
            user_id=TEST_USER_ID,
            operation="create",
            changelog_summary="Initial version",
        )
        await db.commit()

        response = await client.get(f"/api/skills/{skill.id}/revisions")

    assert response.status_code == 200, response.text
    body = response.json()
    assert body[0]["id"] == str(revision.id)
    assert body[0]["operation"] == "create"
    assert body[0]["changelog_summary"] == "Initial version"


